package cn.bmob.sdkdemo;

import cn.bmob.v3.BmobObject;

public class Device extends BmobObject {

    private String sn;
    private String logo;
    private String mac;
    private String device;
    private String pcba;
    private String ageing;
    private String ageingNumber;
    private String machine;
    private String ip;
    private String address;
    private String app;
    private String time;
    private String edition;
    private String activate;
    private String version;

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }



    public String getActivate() {
        return activate;
    }

    public void setActivate(String activate) {
        this.activate = activate;
    }

    public String getEdition() {
        return edition;
    }

    public void setEdition(String edition) {
        this.edition = edition;
    }



    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }



    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getApp() {
        return app;
    }

    public void setApp(String app) {
        this.app = app;
    }




    public String getAgeingNumber() {
        return ageingNumber;
    }

    public void setAgeingNumber(String ageingNumber) {
        this.ageingNumber = ageingNumber;
    }


    public String getAgeing() {
        return ageing;
    }

    public void setAgeing(String ageing) {
        this.ageing = ageing;
    }

    public String getMachine() {
        return machine;
    }

    public void setMachine(String machine) {
        this.machine = machine;
    }



    public String getPcba() {
        return pcba;
    }

    public void setPcba(String pcba) {
        this.pcba = pcba;
    }


    public String getDevice() {
        return device;
    }

    public void setDevice(String device) {
        this.device = device;
    }



    public String getMac() {
        return mac;
    }

    public void setMac(String mac) {
        this.mac = mac;
    }

    public String getSn() {
        return sn;
    }

    public void setSn(String sn) {
        this.sn = sn;
    }

    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }



}
